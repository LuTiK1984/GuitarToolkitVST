    box_f = font(32, True)
    small_f = font(26)
    tiny_f = font(24)

    def box(x1, y1, x2, y2, title, lines, fill="#f2f2f2"):
        d.rounded_rectangle((x1, y1, x2, y2), radius=26, outline="black", width=4, fill=fill)
