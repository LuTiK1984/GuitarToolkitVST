def clear_text_keep_runs(paragraph, text: str) -> None:
    if not paragraph.runs:
        paragraph.add_run(text)
        return
    for run in paragraph.runs:
        run.text = ""
    paragraph.runs[0].text = text


def delete_after_paragraph(doc: Document, keep_index: int) -> None:
    body = doc._body._element
    keep = doc.paragraphs[keep_index]._p
    seen = False
    for child in list(body):
        if child is keep:
            seen = True
            continue
        if seen and child.tag.endswith("}sectPr"):
            continue
        if seen:
            body.remove(child)


def add_p(doc: Document, text: str, style: str = "Normal", align=None):
    p = doc.add_paragraph(text, style=style)
    if align is not None:
        p.alignment = align
    return p


def add_body(doc: Document, text: str):
    return add_p(doc, text, "Normal")


def add_list(doc: Document, text: str):
    p = add_p(doc, f"• {text}", "Normal")
    p.paragraph_format.left_indent = Inches(0.35)
    p.paragraph_format.first_line_indent = Inches(-0.2)
    return p


def add_h1(doc: Document, text: str):
    return add_p(doc, text, "Heading 1")


def add_h2(doc: Document, text: str):
    return add_p(doc, text, "Heading 2")


def add_h3(doc: Document, text: str):
    return add_p(doc, text, "Heading 3")


def add_caption(doc: Document, text: str):
    p = add_p(doc, text, "Caption")
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    return p


def add_image(doc: Document, image_path: Path, caption: str, width_in: float = 5.8):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.add_run().add_picture(str(image_path), width=Inches(width_in))
    add_caption(doc, caption)


def add_code_listing(doc: Document, number: int, title: str, code: str):
    label = add_body(doc, f"Листинг {number}")
    for run in label.runs:
        run.bold = True
    add_body(doc, title)
    for line in code.strip("\n").splitlines():
        p = add_p(doc, line.rstrip(), "Normal")
        p.paragraph_format.first_line_indent = Inches(0)
        p.paragraph_format.left_indent = Inches(0.25)
        p.paragraph_format.line_spacing = 1.0
        for run in p.runs:
            run.font.name = "Courier New"
            run.font.size = Pt(9)


def add_paras(doc: Document, paras: list[str]):
    for para in paras:
        add_body(doc, para)


